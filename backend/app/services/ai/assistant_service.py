def _tree_scope(stmt, user: User):
    if is_platform_admin(user):
        return stmt
    if user.organization_id:
        return stmt.where(
            (Tree.owner_user_id == user.id) | (Tree.organization_id == user.organization_id)
        )
    return stmt.where(Tree.owner_user_id == user.id)


def _bio_scope(stmt, user: User):
    if is_platform_admin(user):
        return stmt
    if user.organization_id:
        return stmt.where(
            (BioacousticRecording.owner_user_id == user.id)
            | (BioacousticRecording.organization_id == user.organization_id)
        )
    return stmt.where(BioacousticRecording.owner_user_id == user.id)


def _fence_scope(stmt, user: User):
    if is_platform_admin(user):
        return stmt
    if user.organization_id:
        return stmt.where(
            (PlantationFence.owner_user_id == user.id)
            | (PlantationFence.organization_id == user.organization_id)
        )
    return stmt.where(PlantationFence.owner_user_id == user.id)


def detect_intent(prompt: str) -> str:
    text = prompt.lower().strip()
    if is_greeting_or_smalltalk(text):
        return "greeting"
    scores: dict[str, int] = {name: 0 for name in INTENT_KEYWORDS}
    for intent, keywords in INTENT_KEYWORDS.items():
        for kw in keywords:
            if kw in text:
                scores[intent] += 1
    best = max(scores, key=scores.get)
    if scores[best] > 0:
        return best
    return "general"


GREETING_PATTERNS = (
    r"^hi\b",
    r"^hello\b",
    r"^hey\b",
    r"^hola\b",
    r"^good\s+(morning|afternoon|evening)\b",
    r"^thanks?\b",
    r"^thank\s+you\b",
    r"^help\b",
    r"^what can you do\b",
    r"^who are you\b",
)


def is_greeting_or_smalltalk(text: str) -> bool:
    normalized = re.sub(r"[^\w\s]", "", text.lower()).strip()
    if not normalized:
        return False
    if len(normalized.split()) <= 3:
        for pattern in GREETING_PATTERNS:
            if re.search(pattern, normalized):
                return True
    return False


def parse_carbon_params(prompt: str, portfolio: PortfolioContext) -> dict[str, Any]:
    text = prompt.lower()
    species = None
    for sp in SPECIES_CATALOG:
        if sp.common_name.lower() in text or sp.scientific_name.lower() in text:
            species = sp.common_name
            break
    if not species and portfolio.top_species:
        species = portfolio.top_species[0][0]

    tree_count = portfolio.total_trees or 1
    for pattern in (
        r"(\d+)\s*(?:trees?|saplings?|plants?)",
        r"(?:trees?|plants?)\s*(?:of|:)?\s*(\d+)",
        r"(\d+)\s*(?:tree|plantation)",
    ):
        m = re.search(pattern, text)
        if m:
            tree_count = int(m.group(1))
            break

    years = 10
    m = re.search(r"(\d+)\s*years?", text)
    if m:
        years = int(m.group(1))

    age_years = None
    m = re.search(r"age[d]?\s*(\d+)", text)
    if m:
        age_years = float(m.group(1))

    return {
        "species": species or "Neem",
        "tree_count": tree_count,
        "years": years,
        "age_years": age_years,
    }


async def build_portfolio_context(
    db: AsyncSession,
    user: User,
    *,
    tree_id: uuid.UUID | None = None,
) -> PortfolioContext:
    ctx = PortfolioContext()

    agg = (
        select(
            func.count(Tree.id).label("total_trees"),
            func.coalesce(func.sum(Tree.current_carbon_kg), 0).label("total_carbon_kg"),
            func.coalesce(
                func.sum(case((Tree.current_health == "healthy", 1), else_=0)),
                0,
            ).label("healthy"),
            func.coalesce(
                func.sum(case((Tree.satellite_verified.is_(True), 1), else_=0)),
                0,
            ).label("sat_verified"),
        )
    )
    row = (await db.execute(_tree_scope(agg, user))).one()
    total_trees = int(row.total_trees)
    total_carbon = float(row.total_carbon_kg or 0)
    total_co2e = total_carbon * (44.0 / 12.0)
    annual_seq = total_co2e * 0.07
    lifetime_credits = (total_co2e * 5) / 1000.0

    ctx.total_trees = total_trees
    ctx.total_carbon_kg = total_carbon
    ctx.total_co2e_kg = total_co2e
    ctx.annual_sequestration_kg = annual_seq
    ctx.lifetime_credits_tco2e = lifetime_credits
    ctx.estimated_revenue_usd = lifetime_credits * 12.0 * 0.55
    ctx.pct_healthy = round((row.healthy / total_trees * 100) if total_trees else 0, 1)
    ctx.pct_satellite_verified = round(
        (row.sat_verified / total_trees * 100) if total_trees else 0, 1
    )

    health_rows = (
        await db.execute(
            _tree_scope(select(Tree.current_health, func.count(Tree.id)).group_by(Tree.current_health), user)
        )
    ).all()
    ctx.health_breakdown = {h or "unknown": int(c) for h, c in health_rows}

    species_rows = (
        await db.execute(
            _tree_scope(
                select(Tree.species_text, func.count(Tree.id))
                .group_by(Tree.species_text)
                .order_by(func.count(Tree.id).desc())
                .limit(5),
                user,
            )
        )
    ).all()
    ctx.top_species = [(s or "Unknown", int(c)) for s, c in species_rows]

    fences = (await db.execute(_fence_scope(select(PlantationFence), user))).scalars().all()
    ctx.fence_count = len(fences)
    ctx.fence_names = [f.name for f in fences[:5]]

    bio_rows = (await db.execute(_bio_scope(select(BioacousticRecording), user))).scalars().all()
    analyzed = [r for r in bio_rows if r.status == "analyzed"]
    ctx.bio_recordings = len(bio_rows)
    if analyzed:
        ctx.bio_avg_health = round(
            sum(float(r.bioacoustic_health_score or 0) for r in analyzed) / len(analyzed), 1
        )
        ctx.bio_avg_shannon = round(
            sum(float(r.shannon_diversity_index or 0) for r in analyzed) / len(analyzed), 3
        )
    ctx.bio_species_detected = len(
        {
            d.get("scientific_name")
            for r in analyzed
            for d in (r.species_detections or [])
            if d.get("scientific_name")
        }
    )

    alert_rows = (
        await db.execute(
            select(Alert)
            .where(Alert.user_id == user.id)
            .order_by(Alert.created_at.desc())
            .limit(5)
        )
    ).scalars().all()
    ctx.unread_alerts = sum(1 for a in alert_rows if not a.is_read)
    ctx.recent_alerts = [
        {"title": a.title, "severity": a.severity, "message": a.message[:180]}
        for a in alert_rows[:3]
    ]

    if tree_id:
        tree = (
            await db.execute(_tree_scope(select(Tree).where(Tree.id == tree_id), user))
        ).scalar_one_or_none()
        if tree:
            ctx.focus_tree = {
                "public_code": tree.public_code,
                "species": tree.species_text,
                "health": tree.current_health,
                "carbon_kg": float(tree.current_carbon_kg or 0),
                "satellite_verified": tree.satellite_verified,
                "dbh_cm": float(tree.current_dbh_cm) if tree.current_dbh_cm else None,
                "height_m": float(tree.current_height_m) if tree.current_height_m else None,
            }

    return ctx


def _answer_portfolio(prompt: str, portfolio: PortfolioContext) -> dict[str, Any]:
    if portfolio.total_trees == 0:
        return {
            "answer": (
                "Your portfolio is empty — no trees are registered yet. "
                "Use **Add tree** or the mobile app to register your first planting, "
                "then I can summarize carbon, health, satellite verification, and biodiversity."
            ),
            "calculations": {"total_trees": 0},
            "citations": ["Aranyix portfolio registry"],
        }

    health_parts = ", ".join(
        f"{count} {status}" for status, count in sorted(portfolio.health_breakdown.items())
    )
    species_line = ", ".join(f"{name} ({count})" for name, count in portfolio.top_species[:3])
    answer = (
        f"You have **{portfolio.total_trees} trees** registered storing "
        f"**{portfolio.total_co2e_kg / 1000:.2f} tCO₂e** "
        f"(~{portfolio.annual_sequestration_kg / 1000:.2f} t/yr projected growth). "
        f"**{portfolio.pct_healthy:.0f}%** are healthy and "
        f"**{portfolio.pct_satellite_verified:.0f}%** are satellite-verified. "
        f"Health mix: {health_parts}. "
        f"Top species: {species_line or 'not tagged yet'}. "
    )
    if portfolio.fence_count:
        answer += (
            f"You monitor **{portfolio.fence_count} plantation site(s)**"
            f" ({', '.join(portfolio.fence_names[:3])}). "
        )
    if portfolio.bio_recordings:
        answer += (
            f"Bioacoustic: **{portfolio.bio_recordings} recordings**, "
            f"**{portfolio.bio_species_detected} fauna species** detected "
            f"(avg health score {portfolio.bio_avg_health}/100). "
        )
    if portfolio.unread_alerts:
        answer += f"You have **{portfolio.unread_alerts} unread alert(s)** — check the Alerts page."

    return {
        "answer": answer,
        "calculations": portfolio.to_dict(),
        "citations": ["Aranyix live portfolio", "IPCC AR6 (carbon projection)"],
    }


def _answer_carbon(prompt: str, portfolio: PortfolioContext) -> dict[str, Any]:
    params = parse_carbon_params(prompt, portfolio)
    result = estimate_carbon(
        CarbonInputs(
            species=params["species"],
            age_years=params.get("age_years"),
        )
    )
    per_tree_annual = result.annual_sequestration_kg or (result.co2e_kg * 0.07)
    years = params["years"]
    trees = params["tree_count"]
    total_t = round(per_tree_annual * trees * years / 1000.0, 2)

    if portfolio.total_trees > 0 and trees == portfolio.total_trees:
        portfolio_note = (
            f" This matches your full registered portfolio of {portfolio.total_trees} trees "
            f"(currently {portfolio.total_co2e_kg / 1000:.2f} tCO₂e stored)."
        )
    else:
        portfolio_note = ""

    answer = (
        f"For **{trees} {params['species']}** trees over **{years} years**, "
        f"the carbon engine estimates **~{total_t} tCO₂e** sequestered "
        f"(~{per_tree_annual:.1f} kg CO₂e/tree/year using {result.methodology}). "
        f"Lifetime credit potential at current tier: "
        f"**{result.lifetime_credits_tco2e:.2f} tCO₂e** "
        f"(~${result.estimated_revenue_usd:.0f} estimated revenue). "
        f"Confidence: {result.confidence:.0%}.{portfolio_note}"
    )

    return {
        "answer": answer,
        "calculations": {
            "species": params["species"],
            "tree_count": trees,
            "years": years,
            "annual_per_tree_kg_co2e": round(per_tree_annual, 2),
            "total_tco2e": total_t,
            "methodology": result.methodology,
            "confidence": result.confidence,
            "portfolio_co2e_t": round(portfolio.total_co2e_kg / 1000, 2),
        },
        "citations": ["IPCC AR6 Vol 4 Ch 4", "Chave et al. 2014", result.methodology],
    }


def _answer_health(portfolio: PortfolioContext) -> dict[str, Any]:
    if portfolio.total_trees == 0:
        return {
            "answer": "Register trees and run AI health analysis on tree photos to see health insights here.",
            "calculations": {},
            "citations": ["Aranyix tree health pipeline"],
        }

    unhealthy = portfolio.health_breakdown.get("unhealthy", 0)
    moderate = portfolio.health_breakdown.get("moderate", 0)
    healthy = portfolio.health_breakdown.get("healthy", 0)
    tips = []
    if unhealthy:
        tips.append(
            f"**{unhealthy} tree(s)** flagged unhealthy — inspect for pests, water stress, or replanting."
        )
    if moderate:
        tips.append(
            f"**{moderate} moderate** — consider balanced NPK and mulch; re-scan in 30 days."
        )
    if not tips:
        tips.append("Portfolio canopy health looks strong. Continue monthly photo checks.")

    focus = ""
    if portfolio.focus_tree:
        t = portfolio.focus_tree
        focus = (
            f" Focus tree **{t['public_code']}** ({t.get('species') or 'species TBD'}): "
            f"status **{t['health']}**, {t['carbon_kg']:.1f} kg carbon stored."
        )

    return {
        "answer": (
            f"**{healthy} healthy**, **{moderate} moderate**, **{unhealthy} unhealthy** "
            f"out of {portfolio.total_trees} trees ({portfolio.pct_healthy:.0f}% healthy). "
            + " ".join(tips)
            + focus
        ),
        "calculations": {
            "health_breakdown": portfolio.health_breakdown,
            "pct_healthy": portfolio.pct_healthy,
        },
        "citations": ["Aranyix AI health classification", "Field scouting recommended"],
    }


def _answer_satellite(portfolio: PortfolioContext) -> dict[str, Any]:
    verified = round(portfolio.pct_satellite_verified, 1)
    pending = max(0, portfolio.total_trees - round(portfolio.total_trees * verified / 100))
    answer = (
        f"**{verified}%** of your trees ({portfolio.total_trees - pending} of {portfolio.total_trees}) "
        f"are satellite-verified. "
    )
    if portfolio.fence_count:
        answer += (
            f"You have **{portfolio.fence_count} plantation fence(s)** configured for NDVI monitoring. "
            "Open the Satellite page to run Sentinel-2 scans and track canopy stress."
        )
    else:
        answer += (
            "No plantation fences yet — draw a site boundary on the Satellite page "
            "to enable NDVI time-series and change detection."
        )
    return {
        "answer": answer,
        "calculations": {
            "pct_satellite_verified": verified,
            "fence_count": portfolio.fence_count,
            "avg_ndvi": portfolio.avg_ndvi,
        },
        "citations": ["Sentinel-2 NDVI", "Aranyix satellite MRV"],
    }


