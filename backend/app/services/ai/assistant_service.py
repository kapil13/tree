def _answer_species(prompt: str, portfolio: PortfolioContext) -> dict[str, Any]:
    text = prompt.lower()
    if "dry" in text or "drought" in text:
        rec = ["Neem", "Amla", "Banyan"]
        reason = "drought-tolerant natives with strong survival in water-stressed sites"
    elif "highway" in text or "road" in text:
        rec = ["Peepal", "Gulmohar", "Neem"]
        reason = "hardy species used in linear infrastructure greening (NHAI-style planting)"
    elif "carbon" in text or "credit" in text:
        rec = ["Teak", "Mango", "Neem"]
        reason = "well-documented allometric curves and IPCC-compatible growth assumptions"
    else:
        rec = ["Neem", "Mango", "Amla", "Bamboo"]
        reason = "versatile species with strong catalog coverage in the Aranyix carbon engine"

    answer = (
        f"For your question, consider **{', '.join(rec)}** — {reason}. "
        f"Your portfolio already includes: "
        f"{', '.join(s for s, _ in portfolio.top_species[:3]) or 'no species tagged yet'}. "
        "Register with GPS + photos so species-specific carbon and health models apply."
    )
    return {
        "answer": answer,
        "calculations": {"recommended_species": rec},
        "citations": ["Aranyix species catalog", "IPCC AR6 allometrics"],
    }


